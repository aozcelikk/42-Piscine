char	*ft_strlowcase(char *str)
{
	int	a;

	a = 0;
	while (str[a] != '\0')
	{
		if (str[a] > 64 && str[a] < 91)
		{
			str[a] += 32;
		}
		a++;
	}
	return (str);
}
